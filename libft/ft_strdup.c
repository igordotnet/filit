#include "libft.h"

char		*ft_strdup(const char *s1)
{
	size_t	i;
	char	*ptr;

	i = ft_strlen(s1);
	if (!(ptr = (char *)malloc(sizeof(*ptr) * (i + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
